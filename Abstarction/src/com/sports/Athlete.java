package com.sports;

public abstract class Athlete {
	String name;
	String nickName;
	int yearOfBorn;
	String team;
	int noOfCompetetions;
	
	String getName() {
		return name;
	}
	String getnickName() {
		return nickName;
	}
	int getyearOfBorn() {
		return yearOfBorn;
	}
	String getTeam() {
		return team;
	}
	int getnoOfCompetetions() {
		return noOfCompetetions;
	}
	

}
